package L09_Conditonals_Loops;

public class Line_by_line_execution {
    public static void main(String[] args) {
        int num = 1;
        do {
            System.out.print("Hello World");
        } while (num != 1);
    }
}
/*
here there is no update statement but the program will still be
executed.
it will execute the print function at least once then it will stop because it
is a line by line execution
 */
